#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	unsigned char	*str;
	int				size;

	size = 0;
	str = (unsigned char *)b;
	while (len-- > 0)
	{
		str[size++] = '\0';
	}
	return (b);
}
